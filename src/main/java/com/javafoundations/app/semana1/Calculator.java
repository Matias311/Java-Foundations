package com.javafoundations.app.semana1;

/** Calculator. */
public class Calculator {

  /**
   * adds two numbers.
   *
   * @param num1 int
   * @param num2 int
   * @return int
   */
  public int add(int num1, int num2) {
    return num1 + num2;
  }

  /**
   * subtract two numbers.
   *
   * @param num1 int
   * @param num2 int
   * @return int
   */
  public int subtraction(int num1, int num2) {
    return num1 - num2;
  }

  /**
   * multiply two numbers.
   *
   * @param num1 int
   * @param num2 int
   * @return int
   */
  public int multiplication(int num1, int num2) {
    return num1 * num2;
  }

  /**
   * divide two numbers.
   *
   * @param num1 int
   * @param num2 int
   * @return int
   */
  public int division(int num1, int num2) {
    return num1 / num2;
  }
}
